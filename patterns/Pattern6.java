class Pattern6 {
  public static void main(String[] args) {
    int k = 6;
    for (int i = 1; i < k; i++) {

      for (int j = 2 * (k - i); j >= 0; j--) {

        System.out.print(" ");

      }
      for (int j = 0; i <= i; j++) {
        System.out.print("*");
      }

    }

  }
}